    public boolean isEmail(String email)
    {
        String regex = "\\w+@\\w+\\.\\w+";
        if(email.matches(regex) == false)
        {
            return false;
        }
        return true;
    }
    
    public boolean isNumber(String number)
    {
        try{
            
            int so = Integer.parseInt(number);
            if(so < 0)
            {
                return false;
            }
        }
        catch(Exception e)
        {
            return false;
        }
        return true;
    }
    
    public boolean isNull() {
        String name = txt_uname.getText();
        String email = txt_uemail.getText();
        String urlImage = txt_uimage.getText();
        if (name.trim().length() == 0 || email.trim().length() == 0 || urlImage.trim().length() == 0) {
            return false;
        }
        return true;
    }

