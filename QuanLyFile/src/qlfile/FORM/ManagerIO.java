    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        try {
            if(viTri < 0)
            {
                JOptionPane.showConfirmDialog(null, "Vui lòng chọn dòng để xóa", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                String name = txt_uname.getText();
                lUser.remove(viTri);
                String sql = "{call deleteUserInfo('"+name+"')}";
                CallableStatement cas = connect.prepareCall(sql);
                cas.executeQuery();
                
                JOptionPane.showConfirmDialog(null, "Xóa thành công", "Thông báo", JOptionPane.ERROR_MESSAGE);
                lamMoi();
                loadUser();
            }
        } catch (SQLException ex) {
            JOptionPane.showConfirmDialog(null, "Bạn không có quyền thao tác này!!!", "Thông báo", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(ManagerIO.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

    private void btn_suaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_suaActionPerformed
        try {
            if(viTri < 0)
            {
                JOptionPane.showConfirmDialog(null, "Vui lòng chọn dòng để cập nhật", "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
            else
            {
                String name = txt_uname.getText();
                String phone = txt_uemail.getText();
                String urlImage = txt_uimage.getText().toString();
                User user = lUser.get(viTri);
                int id = Integer.parseInt(user.getU_id());
                String sql = "{call updateUserInfo("+id+", '"+name+"','"+phone+"','"+urlImage+"')}";
                capNhatListUser(name, phone, urlImage);
                CallableStatement cas = connect.prepareCall(sql);
                cas.executeQuery();
                JOptionPane.showConfirmDialog(null, "Cập nhật thành công", "Thông báo", JOptionPane.ERROR_MESSAGE);
                lamMoi();
                loadUser();
            }
        } catch (SQLException ex) {
            JOptionPane.showConfirmDialog(null, "Bạn không có quyền thao tác này", "Thông báo", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(ManagerIO.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btn_suaActionPerformed

