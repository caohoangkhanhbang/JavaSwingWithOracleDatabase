    private void btn_jfileChooserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_jfileChooserActionPerformed
        JFileChooser jfileChooser = new JFileChooser();
        jfileChooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int result = jfileChooser.showOpenDialog(null);
        if(result == JFileChooser.APPROVE_OPTION)
        {
            File file = jfileChooser.getSelectedFile();
            filePath = file.getAbsolutePath().toString();
            txt_uimage.setText(filePath);
            
//            Hiển thị ảnh lên label
            ImageIcon image = new ImageIcon(new ImageIcon(filePath).getImage().getScaledInstance(lbl_anh.getWidth(), lbl_anh.getHeight(), Image.SCALE_SMOOTH));
            lbl_anh.setIcon(image);
        }
    }//GEN-LAST:event_btn_jfileChooserActionPerformed

