    public void loadUser(){
        String sql = "{call getUserInfomation(?)}";
        try {
            CallableStatement cas = connect.prepareCall(sql);
            cas.registerOutParameter(1, OracleTypes.CURSOR);
            cas.executeQuery();
            ResultSet rs = (ResultSet) cas.getObject(1);
            model.setRowCount(0);
            model.setColumnIdentifiers(new Object[]{"U_ID", "U_NAME", "U_PHONE", "U_IMAGAE"});
            lUser.clear();
            while (rs.next()) {
                model.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)});
                lUser.add(new User(rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4)));
            }
        } catch (SQLException ex) {
            JOptionPane.showConfirmDialog(null, "Bạn không có quyền thao tác ", "Cảnh báo", JOptionPane.CANCEL_OPTION);
//            this.dispose();
        }
    }

