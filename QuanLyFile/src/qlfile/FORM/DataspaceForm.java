    private void btnLoad2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoad2ActionPerformed
        try {
            CallableStatement callableStatement=connection2.prepareCall("{call GET_DATA_FILE(?,?)}");
            callableStatement.setString(1,cbbFileid.getSelectedItem().toString());
            callableStatement.registerOutParameter(2, OracleTypes.CURSOR);
            callableStatement.execute();
            
            ResultSet resultSet=(ResultSet)callableStatement.getObject(2);
            var data = new ArrayList<>();
            while (resultSet.next()) {
                int index = data.size() + 1;
                String[] row = new String[4];
                row[0] = String.valueOf(index++);
                row[1] = resultSet.getString("FILE_NAME");
                row[2] = resultSet.getString("FILE_ID");
                row[3] = resultSet.getString("TABLESPACE_NAME");

                data.add(row);
            }
            TableModel model = new DefaultTableModel(
                data.toArray(new String[][] {}), // Convert to array of arrays
                new String[] {"STT","FILE_NAME", "FILE_ID","TABLESPACE_NAME"} // Set column names
            );
            datafileTable.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnLoad2ActionPerformed

    private void datafileTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_datafileTableMouseClicked
        int row = datafileTable.getSelectedRow();
        // Lấy giá trị từ mỗi ô trong dòng được click và in ra
        for (int col = 0; col < datafileTable.getColumnCount(); col++) {
            Object value = datafileTable.getValueAt(row, col);
            System.out.println("Column " + col + ": " + value);
            if(col==1){
                txtFilename.setText(value.toString());
            }else if(col==3){
                txtTablespacename.setText(value.toString());
            }
        }
    }//GEN-LAST:event_datafileTableMouseClicked

