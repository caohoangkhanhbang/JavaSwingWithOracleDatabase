    private void Load3(){
        try {
            CallableStatement callableStatement=connection2.prepareCall("{call GET_FILE_TABLESPACENAME(?)}");
            callableStatement.registerOutParameter(1, OracleTypes.CURSOR);
                callableStatement.execute();
                ResultSet resultSet=(ResultSet)callableStatement.getObject(1);
                
                
                var data = new ArrayList<>();
                while (resultSet.next()) {
                    int index = data.size() + 1;
                    String[] row = new String[4];
                    row[0] = String.valueOf(index++);
                    row[1] = resultSet.getString("FILE_NAME");
                    row[2] = resultSet.getString("FILE_ID");
                    row[3] = resultSet.getString("TABLESPACE_NAME");

                    data.add(row);
                }
                TableModel model = new DefaultTableModel(
                    data.toArray(new String[][] {}), // Convert to array of arrays
                    new String[] {"STT","File name", "File id","Tablespace name"} // Set column names
                );
                Tablespace.setModel(model);
        } catch (SQLException e) {
            e.printStackTrace();
        } 
    }
    
