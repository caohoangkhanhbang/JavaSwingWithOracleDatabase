    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed

        try {
            String id = txtID.getText();
            String sql = "DELETE FROM PROJECT.FILES WHERE F_ID=?";

            PreparedStatement preparedStatement = connection.prepareStatement(sql);
            preparedStatement.setString(1, id);

            int rowsDeleted = preparedStatement.executeUpdate();

            if (rowsDeleted > 0) {
                JOptionPane.showMessageDialog(null, "Xóa thành công");
            } else {
                JOptionPane.showMessageDialog(null, "Không tìm thấy thư mục để xóa");
            }

            preparedStatement.close();
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "User "+username+" Không có quyền xóa dữ liệu trong bảng FILES");
        }
        LoadTable();
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        String id=txtID.getText();
        String loai=txtLoaifile.getText();
        String mota=txtMoTa.getText();
        String ten=txtTen.getText();
        
        try {
            String callProcedure = "{call UPDATE_FILE_PROCEDURE(?, ?, ?, ?, ?)}";
            CallableStatement cstmt;
            cstmt = connection.prepareCall(callProcedure);
            cstmt.setInt(1, Integer.parseInt(id)); // ID của tệp cần sửa
            cstmt.setString(2, ten);
            cstmt.setString(3, loai);
            cstmt.setString(4, mota);
            cstmt.setInt(5, 1); // Cờ cho biết tệp đã bị xóa hay không (0: không, 1: có)

            // Thực thi procedure
            cstmt.execute();
            JOptionPane.showMessageDialog(null, "Sửa thành công !");
        } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "User "+username+" Không có quyền sửa dữ liệu trong bảng FILE");
        }
        LoadTable();
    }//GEN-LAST:event_btnSuaActionPerformed

