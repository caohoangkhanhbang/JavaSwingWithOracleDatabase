    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        String id=txtID.getText();
        String loai=txtLoaifile.getText();
        String mota=txtMoTa.getText();
        
        if(txtID.getText()==""){
            JOptionPane.showMessageDialog(null, "Vui lòng nhập File ID");
        }
        else{
            try {
                String callProcedure = "{call INSERT_FILE_PROCEDURE(?, ?, ?, ?, ?, ?)}";
                CallableStatement cstmt;
                cstmt = connection.prepareCall(callProcedure);
                cstmt.setInt(1, Integer.parseInt(id));
                cstmt.setString(2,duongdananh);
                cstmt.setString(3, loai);
                cstmt.setDate(4, new java.sql.Date(System.currentTimeMillis())); // SYSDATE
                cstmt.setString(5, mota);
                cstmt.setInt(6, 0);
                cstmt.execute();
                JOptionPane.showMessageDialog(null, "Thêm thành công !");

            } catch (SQLException ex) {
                Logger.getLogger(CRUD_FileForm.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(this, "User "+username+" Không có quyền thêm dữ liệu trong bảng FILES");
            }
        }  
        LoadTable();
    }//GEN-LAST:event_btnThemActionPerformed

