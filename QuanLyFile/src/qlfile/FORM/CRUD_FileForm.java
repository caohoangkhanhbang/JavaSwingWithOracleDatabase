    private void LoadTable(){
        try {
            Statement statement = connection.createStatement();

            ResultSet resultSet = statement.executeQuery("SELECT *FROM PROJECT.FILES");

            var data = new ArrayList<>();
            while (resultSet.next()) {
                String[] row = new String[6];
                row[0] = resultSet.getString("F_ID");
                row[1] = resultSet.getString("F_NAME");
                row[2] = resultSet.getString("F_TYPE");
                row[3] = resultSet.getString("F_DATECREATE");
                row[4] = resultSet.getString("F_DESCRIBE");
                //row[5] = resultSet.getString("F_PATH");
                data.add(row);
            }
            TableModel model = new DefaultTableModel(
                data.toArray(new String[][] {}), // Convert to array of arrays
                new String[] {"File ID", "Tên File","Loại File","Ngày tạo","Mô tả","Đường dẫn"} // Set column names
            );
            tblFile.setModel(model);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "User "+username+" Không có quyền truy cập bảng FILES");
        }
    }
    
