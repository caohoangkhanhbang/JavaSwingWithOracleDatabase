    private void btn_taoProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_taoProfileActionPerformed
        String tenProfile = txt_ten.getText();
        String thoiGianKhoa = txt_thoiGianKhoa.getText();
        String thoiGianSD = txt_thoiGianSD.getText();
        String soLanDoiPass = txt_soLanDoiPass.getText();
        String soLanSaiMatKhau = txt_soLanSaiMK.getText();
        String soSession = txt_soSession.getText();
        if(tenProfile.trim().length()<1 ||  thoiGianKhoa.trim().length()<1|| thoiGianSD.trim().length()<1||
                soLanDoiPass.trim().length()<1 || soLanSaiMatKhau.trim().length()<1)
        {
            JOptionPane.showConfirmDialog(this, "Hãy điền đầy đủ vào các trường để tạo profile");
        }
        else
        {
            try {
                CallableStatement callStatement = con.prepareCall("{call sp_taoProfile(?, ?, ?, ?, ?, ?)}");
                callStatement.setString(1, tenProfile);
                callStatement.setInt(2, Integer.parseInt(soSession));
                callStatement.setInt(3, Integer.parseInt(thoiGianSD));
                callStatement.setInt(4, Integer.parseInt(soLanDoiPass));
                callStatement.setInt(5, Integer.parseInt(soLanSaiMatKhau));
                callStatement.setInt(6, Integer.parseInt(thoiGianKhoa));
                callStatement.executeQuery();
                loadProfile();
                txt_ten.setText("");
                JOptionPane.showConfirmDialog(this, "Tạo mới profile: "+ tenProfile + " thành công", "Thông báo", 
                        JOptionPane.OK_OPTION);
            } catch (SQLException ex) {
                Logger.getLogger(CreateProfileUser.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showConfirmDialog(this, "Tạo thất bại, có thể do trùng tên profile, hãy thử với tên khác");
            }
        }
    }//GEN-LAST:event_btn_taoProfileActionPerformed

