    public void loadProfile()
    {
         try {
             String sql = "select * from dba_profiles";
             maha = new MahaFunction();
             ResultSet rs = maha.ExeCuteQuery(sql);
             DefaultTableModel model = (DefaultTableModel) tbl_profile.getModel();
             model.setRowCount(0);
             model.setColumnIdentifiers(new Object[]{"PROFILE", "RESOURCE_NAME", "RESOURC_TYPE", "LIMIT", "COMMON"});
             while(rs.next())
             {
                 Object obj[] = {rs.getString("PROFILE"), rs.getString("RESOURCE_NAME"), rs.getString("RESOURCE_TYPE"), rs.getString("LIMIT"), rs.getString("COMMON")};
                 model.addRow(obj);
             }} catch (SQLException ex) {
             Logger.getLogger(CreateProfileUser.class.getName()).log(Level.SEVERE, null, ex);
         }
    }
    
