    private void btn_xoaProfileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaProfileActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbl_profile.getModel();
        //getSelectedRow dùng để lấy ra vị trí dòng đang chọn trên table
        int dong = tbl_profile.getSelectedRow();
        String tenProfile = (String)model.getValueAt(dong, 0);
        if(tenProfile.trim().length() < 1)
        {
            //Dòng lệnh này chưa có hiệu lực
            JOptionPane.showConfirmDialog(null, "Vui lòng chọn tên profile trên bảng để xóa", "Thông báo", JOptionPane.OK_OPTION);
        }
        if(tenProfile.trim().length() > 0)
        {
            txt_ten.setText(tenProfile);
            if (JOptionPane.showConfirmDialog(null, "Bạn có chắc muốn xóa profile " + tenProfile, "Xác nhận", 
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.WARNING_MESSAGE) == JOptionPane.OK_OPTION) {
                try {
                    String sql = "{call  xoaprofile('" + tenProfile + "')}";
                    CallableStatement call = con.prepareCall(sql);
                    call.executeQuery();
                    JOptionPane.showConfirmDialog(null, "Xóa thành công profile " + tenProfile, "Thông báo", JOptionPane.OK_OPTION);
                    loadProfile();
                    call.close();
                } catch (SQLException ex) {
                    Logger.getLogger(CreateProfileUser.class.getName()).log(Level.SEVERE, null, ex);
                    JOptionPane.showConfirmDialog(null, "Xóa thất bại", "Thông báo", JOptionPane.OK_OPTION);
                }
            }
        }
    }//GEN-LAST:event_btn_xoaProfileActionPerformed

